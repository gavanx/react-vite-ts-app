var paintWalls = function (cost, time) {
  const n = cost.length
  const map = new Map()
  const dfs = (i, t) => {
    if (i === n) return t >= 0 ? 0 : Infinity
    if (t >= n - i) {
      return 0
    }
    let key = `${i},${t}`
    if (map.has(key)) return map.get(key)
    const res = Math.min(dfs(i + 1, t - 1), cost[i] + dfs(i + 1, t + time[i]))
    map.set(key, res)
    return res
  }
  return dfs(0, 0)
}

